/*
 * 快速平方根近似。
 *
 * 这个算法来自经典的 fast inverse square root 思路：
 *   先快速近似 1/sqrt(number)，再乘以 number，得到 sqrt(number)。
 *
 * 优点：快。
 * 缺点：有误差；本工程用于 SVPWM 电压幅值估算，通常可以接受。
 */
float _sqrtApprox(float number) {//low in fat
    long i;
    float y;
    // float x;
    // const float f = 1.5F; // better precision

    // x = number * 0.5F;
    y = number;
    i = * ( long * ) &y;
    i = 0x5f375a86 - ( i >> 1 );
    y = * ( float * ) &i;
    // y = y * ( f - ( x * y * y ) ); // better precision
    // number * (1/sqrt(number)) 约等于 sqrt(number)。
    return number * y;
}
