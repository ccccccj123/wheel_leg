// 把任意弧度角归一化到 [0, 2PI)。
float _normalizeAngle(float angle){
    // fmod 得到除以 2PI 后的余数；如果是负数，再补一个 2PI。
    float a = fmod(angle, _2PI);
    return a >= 0 ? a : (a + _2PI);
}
