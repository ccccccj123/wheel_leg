/*
 * 使用查表方式近似计算 sin(a)。
 *
 * 输入要求：
 *   a 必须已经被限制在 0~2PI。
 *
 * 输出：
 *   近似的 sin(a)，误差大约在 0.005 量级。
 */
float _sin(float a){
    if(a < _PI_2){
        // 第一象限：直接查表。126.6873 约等于 199 / (PI/2)。
        return 0.0001*sine_array[_round(126.6873* a)];      // int array optimized
    }else if(a < _PI){
        // 第二象限：sin(PI - x) = sin(x)，所以反向查表。
        return 0.0001*sine_array[398 - _round(126.6873*a)];     // int array optimized
    }else if(a < _3PI_2){
        // 第三象限：正弦为负，形状与第一象限相同。
        return -0.0001*sine_array[-398 + _round(126.6873*a)];      // int array optimized
    } else {
        // 第四象限：正弦为负，形状与第二象限相同。
        return -0.0001*sine_array[796 - _round(126.6873*a)];      // int array optimized
    }
}
